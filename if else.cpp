//find character is vowel or consonant
#include<iostream>
using namespace std;
int main()
{
	char value;
	cout<<"Enter a character: ";
	cin>>value;
	if(value=='a')
	{
		cout<<"vowel";
	}
	else if(value=='e')
	{
		cout<<"vowel";
	}
	else if(value=='i')
	{
		cout<<"vowel";
	}
	else if(value=='o')
	{
		cout<<"vowel";
	}
	else if(value=='u')
	{
		cout<<"vowel";
	}
	else
	{
		cout<<"consonant";
	}
	return 0;
}
